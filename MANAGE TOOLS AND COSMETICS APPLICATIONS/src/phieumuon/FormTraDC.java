    private void btn_SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SubmitActionPerformed
        // TODO add your handling code here:
        LichSuHu lsh = new LichSuHu();
        LichSuHu lsh1 = new LichSuHu();
        Date date = Date.valueOf(ngayMuon);  
        for(int i = 0;;i++){
            try{
                if(jTable1.getValueAt(i,0) == null){
                    break;
                }
                else if(Integer.parseInt(jTable1.getValueAt(i,3).toString()) != 0){
                    lsh.setIdMatHang(jTable1.getValueAt(i,0).toString());
                    lsh.setSoLuongHu(Integer.parseInt(jTable1.getValueAt(i,3).toString()));
                    lsh.setNgayHu(date);
                    ConnectionSQL sql = new ConnectionSQL();
                    lsh1 = sql.getPK(lsh.getIdMatHang(), ngayMuon);
                    if(lsh1.getIdMatHang() == null){
                        sql.insertSLH(lsh);
                        String updatePM = "update phieumuon set datra = 'True' where idpm = '" +
                                qlpm.getPhieuMuon().getIdPM() + "'";
                        sql.updateSQL(updatePM);
                    }
                    else{
                        int soLuongHu = lsh.getSoLuongHu() + lsh1.getSoLuongHu();
                        String update = "update lichsuhu set soluonghu = '" +
                                soLuongHu + "' where idmathang = '" + lsh1.getIdMatHang() + "' "
                                + "and ngayhu = '" + lsh1.getNgayHu() + "'";
                        sql.updateSQL(update);
                        String updatePM = "update phieumuon set datra = 'True' where idpm = '" +
                                qlpm.getPhieuMuon().getIdPM() + "'";
                        sql.updateSQL(updatePM);
                    }
                }
                else if(Integer.parseInt(jTable1.getValueAt(i,3).toString()) == 0){                    
                    ConnectionSQL sql = new ConnectionSQL();
                    String updatePM = "update phieumuon set datra = 'True' where idpm = '" +
                                qlpm.getPhieuMuon().getIdPM() + "'";
                        sql.updateSQL(updatePM);
                }
            }
            catch(ArrayIndexOutOfBoundsException ex){
                break;
            }
        }
        this.dispose();
        qlpm.loadData();
    }//GEN-LAST:event_btn_SubmitActionPerformed

    private void text_SLHuKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_text_SLHuKeyReleased
        // TODO add your handling code here:
        String text = text_SLHu.getText();
        char a = text.charAt(text.length() - 1);
        if(!KTinput(Character.toString(a)) || text_SLHu.getText().equals("0") || text_SLHu.getText().equals(" ")){
            text_SLHu.setText("");
            JOptionPane.showMessageDialog(rootPane, "Bạn chỉ được nhập số lớn hơn 0!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        InfoCTPM info = this.getInfo();
        if(Integer.parseInt(text) > info.getSoLuong()){
            text_SLHu.setText("");
            JOptionPane.showMessageDialog(rootPane, "Đã vượt quá số lượng mượn!", "Lỗi", JOptionPane.ERROR_MESSAGE);            
        }
    }//GEN-LAST:event_text_SLHuKeyReleased

