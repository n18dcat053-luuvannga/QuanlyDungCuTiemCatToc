    //convert string dd-mm-yyyy to yyyy-mm-dd
    public String convertStrToStr(String strDD){
        String day,year;
        day = strDD.split("-")[0];
        year = strDD.split("-")[2];
        String strYYYY = year + "-" +strDD.split("-")[1] + "-" + day;
        return strYYYY;
    }
    
    //load du lieu len form
    public void loadData(QuanLiPhieuMuon aThis,int slHu){
        ctphieumuon.ConnectionSQL sqlCTPM = new ctphieumuon.ConnectionSQL();
        String idPM = aThis.getPhieuMuon().getIdPM();
        ArrayList<InfoCTPM> list = sqlCTPM.getListCTPM(idPM);
        model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // xoa bo noi dung cu cua table
        for (InfoCTPM info : list) {
            Object[] data = {info.getIdmathang(),info.getTenmathang(),info.getSoLuong(),slHu};
            listSLHu.add(slHu);
            model.addRow(data);
        }
    }
    
    //load du lieu len form sau khi update soluonghu
    public void loadData(QuanLiPhieuMuon aThis,int slHu,String idMH){
        ctphieumuon.ConnectionSQL sqlCTPM = new ctphieumuon.ConnectionSQL();
        String idPM = aThis.getPhieuMuon().getIdPM();
        ArrayList<InfoCTPM> list = sqlCTPM.getListCTPM(idPM);
        model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // xoa bo noi dung cu cua table
        int i = 0;
        for (InfoCTPM info : list) {
            if(info.getIdmathang().equals(idMH)){
                listSLHu.set(i, slHu);
                i++;
            }
            else i++;
        }
        i = 0;
        for (InfoCTPM info : list) {
            Object[] data = {info.getIdmathang(),info.getTenmathang(),info.getSoLuong(),listSLHu.get(i)};
            model.addRow(data);
            i++;
        }
    }
    
    //lay du lieu tu 1 hang cua table
    public InfoCTPM getDataARow(){
        InfoCTPM info = new InfoCTPM();
        int i = jTable1.getSelectedRow();
        info.setIdmathang((String) jTable1.getValueAt(i,0));
        info.setTenmathang((String) jTable1.getValueAt(i,1));
        Object a =  jTable1.getValueAt(i,2);
        info.setSoLuong((int) a);
        return info;
    } 
    
