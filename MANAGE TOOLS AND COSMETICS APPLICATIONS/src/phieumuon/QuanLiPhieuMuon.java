    public void loadData(){
        ConnectionSQL sql = new ConnectionSQL();
        //test tinh nang khi co user se xu li
//        String idpq = "PQ1",idnv = "NV3";
        SimpleDateFormat format1 = new SimpleDateFormat("dd-MM-yyyy");
        dsPhieuMuon =  sql.getListPM();        
        model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // xoa bo noi dung cu cua table
        if(idPQ.equals("PQ2")){
            for (PhieuMuon pm : dsPhieuMuon) {
                if(pm.getIdNV().equals(idNV)){
                    String ten = null;
                    try {
                        String dbURL = "jdbc:sqlserver://localhost;databaseName=QL_DCMPTCT;user=sa;password=123";
                        java.sql.Connection conn = DriverManager.getConnection(dbURL);
                        Statement stmt = conn.createStatement();
                        String select = "select ten from nhanvien where idnv = '" + pm.getIdNV() +"'";
                        ResultSet rs = stmt.executeQuery(select);
                        while(rs.next()){
                            ten = rs.getString("ten");
                        }   
                    } catch (SQLException ex) {
                        System.err.println("Cannot connect database, " + ex);
                    }
                String daTra;
                if(pm.isDaTra()) daTra = "Đã trả";
                else daTra = "Chưa trả";
                Object[] data = {pm.getIdPM(),format1.format(pm.getNgayMuon()),ten,daTra}; 
                    model.addRow(data);
                }
            }
        }
        else{
            for (PhieuMuon pm : dsPhieuMuon) {
                String ten = null;
                try {
                    String dbURL = "jdbc:sqlserver://localhost;databaseName=QL_DCMPTCT;user=sa;password=123";
                    java.sql.Connection conn = DriverManager.getConnection(dbURL);
                    Statement stmt = conn.createStatement();
                    String select = "select ten from nhanvien where idnv = '" + pm.getIdNV() +"'";
                    ResultSet rs = stmt.executeQuery(select);
                    while(rs.next()){
                        ten = rs.getString("ten");
                    }   
                } catch (SQLException ex) {
                    System.err.println("Cannot connect database, " + ex);
                }
                String daTra;
                if(pm.isDaTra()) daTra = "Đã trả";
                else daTra = "Chưa trả";
                Object[] data = {pm.getIdPM(),format1.format(pm.getNgayMuon()),ten,daTra};                 
                model.addRow(data);
            }
        }
    }
    
    //lay du lieu tu 1 hang cua table
    public PhieuMuon getDataARow(){
        PhieuMuon pm = new PhieuMuon();
        try {
            SimpleDateFormat format1 = new SimpleDateFormat("yyyy-MM-dd");
            int i = jTable1.getSelectedRow();
            pm.setIdPM((String) jTable1.getValueAt(i,0));
            pm.setNgayMuon(format1.parse((String) jTable1.getValueAt(i,1)));
            pm.setIdNV((String) jTable1.getValueAt(i,2));
        } catch (ParseException ex) {
            Logger.getLogger(QuanLiPhieuMuon.class.getName()).log(Level.SEVERE, null, ex);
        }
        return pm;
    } 
