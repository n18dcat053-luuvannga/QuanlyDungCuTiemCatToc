    private void delButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delButtonActionPerformed
        // TODO add your handling code here:
        if(this.getPhieuMuon() == null){
            JOptionPane.showMessageDialog(rootPane, "Phải chọn 1 phiếu mượn để xóa!", "Lỗi", JOptionPane.ERROR_MESSAGE);            
            return;
        }
        int option = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắn chắn muốn xóa phiếu mượn này?", "Cảnh báo",JOptionPane.YES_NO_OPTION);
        if (option == JOptionPane.YES_OPTION){
            ConnectionSQL sql = new ConnectionSQL();
            String idPM = phieumuon.getIdPM();
            try{
                sql.deleteSQL(idPM);
            }
            catch(Exception ex){
                JOptionPane.showMessageDialog(rootPane, "Đã xảy ra lỗi khi xóa phiếu !", "Lỗi", JOptionPane.ERROR_MESSAGE);                        
            }
            loadData();
            JOptionPane.showMessageDialog(rootPane, "Phiếu mượn đã xóa thành công!", "Message", JOptionPane.INFORMATION_MESSAGE);
        }            
    }//GEN-LAST:event_delButtonActionPerformed

